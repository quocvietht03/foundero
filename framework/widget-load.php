<?php

namespace FounderoElementorWidgets;

/**
 * Class ElementorWidgets
 *
 * Main ElementorWidgets class
 * @since 1.0.0
 */
class ElementorWidgets
{

	/**
	 * Instance
	 *
	 * @since 1.0.0
	 * @access private
	 * @static
	 *
	 * @var ElementorWidgets The single instance of the class.
	 */
	private static $_instance = null;

	/**
	 * Instance
	 *
	 * Ensures only one instance of the class is loaded or can be loaded.
	 *
	 * @since 1.0.0
	 * @access public
	 *
	 * @return ElementorWidgets An instance of the class.
	 */
	public static function instance()
	{
		if (is_null(self::$_instance)) {
			self::$_instance = new self();
		}
		return self::$_instance;
	}

	public $widgets = array();

	public function widgets_list()
	{

		$this->widgets = array(
			'mobile-menu',
			'site-information',
			'site-social',
			'site-copyright',
			'page-breadcrumb',
			'menu-business',

		);

		return $this->widgets;
	}

	/**
	 * widget_styles
	 *
	 * Load required core files.
	 *
	 * @since 1.0.0
	 * @access public
	 */
	public function widget_styles()
	{
		// https://developer.wordpress.org/reference/functions/wp_enqueue_style/
		// wp_enqueue_style( $handle, $src, $deps, $ver, $media );
	}

	/**
	 * widget_scripts
	 *
	 * Load required core files.
	 *
	 * @since 1.0.0
	 * @access public
	 */
	public function widget_scripts()
	{
		// https://developer.wordpress.org/reference/functions/wp_register_script/
		// wp_register_script( $handle, $src, $deps, $ver, $args );

		wp_register_script('foundero-widgets',  get_stylesheet_directory_uri() . '/framework/widgets/frontend.js', ['jquery'], '', true);
	}

	/**
	 * Include Widgets files
	 *
	 * Load widgets files
	 *
	 * @since 1.0.0
	 * @access private
	 */
	private function include_widgets_files()
	{

		foreach ( $this->widgets_list() as $widget ) {
			$widget_file = get_stylesheet_directory() . '/framework/widgets/' . $widget . '/widget.php';
			
			// Only require if file exists and class is not already declared
			if ( file_exists( $widget_file ) && ! class_exists( $widget ) ) {
				require_once $widget_file;
			}

			// Include skins safely
			$skins_path = get_stylesheet_directory() . '/framework/widgets/' . $widget . '/skins/';
			foreach ( glob( $skins_path . '*.php' ) as $filepath ) {
				if ( file_exists( $filepath ) ) {
					include $filepath;
				}
			}
		}
	}

	/**
	 * Register categories
	 *
	 * Register new Elementor category.
	 *
	 * @since 1.0.0
	 * @access public
	 */
	public function register_categories($elements_manager)
	{

		$elements_manager->add_category(
			'bt-foundero',
			[
				'title' => esc_html__('Foundero', 'foundero')
			]
		);
	}

	/**
	 * Register Widgets
	 *
	 * Register new Elementor widgets.
	 *
	 * @since 1.0.0
	 * @access public
	 */
	public function register_widgets()
	{
		// Its is now safe to include Widgets files
		$this->include_widgets_files();

		// Register Widgets
		\Elementor\Plugin::instance()->widgets_manager->register_widget_type(new Widgets\FounderoMobileMenu\Widget_FounderoMobileMenu());
		\Elementor\Plugin::instance()->widgets_manager->register_widget_type(new Widgets\FounderoSiteInformation\Widget_FounderoSiteInformation());
		\Elementor\Plugin::instance()->widgets_manager->register_widget_type(new Widgets\FounderoSiteSocial\Widget_FounderoSiteSocial());
		\Elementor\Plugin::instance()->widgets_manager->register_widget_type(new Widgets\FounderoSiteCopyright\Widget_FounderoSiteCopyright());
		\Elementor\Plugin::instance()->widgets_manager->register_widget_type(new Widgets\FounderoPageBreadcrumb\Widget_FounderoPageBreadcrumb());
		\Elementor\Plugin::instance()->widgets_manager->register_widget_type(new Widgets\FounderoMenuBusiness\Widget_FounderoMenuBusiness());
	}

	/**
	 *  ElementorWidgets class constructor
	 *
	 * Register action hooks and filters
	 *
	 * @since 1.0.0
	 * @access public
	 */
	public function __construct()
	{

		// Register widget styles
		add_action('elementor/frontend/after_register_styles', [$this, 'widget_styles']);

		// Register widget scripts
		add_action('elementor/frontend/after_register_scripts', [$this, 'widget_scripts']);

		// Register categories
		add_action('elementor/elements/categories_registered', [$this, 'register_categories']);

		// Register widgets
		add_action('elementor/widgets/widgets_registered', [$this, 'register_widgets']);
	}
}

// Instantiate ElementorWidgets Class
ElementorWidgets::instance();
